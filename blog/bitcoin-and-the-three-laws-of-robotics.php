	</center>


	<p>
		You may not have considered it, but Bitcoin can be viewed as an
		unmanned company – or a
		<i>Distributed Autonomous Corporation</i> (DAC) if you prefer.
	</p>

	<p>
		Unlike passive currencies, Bitcoin derives much of its tangible value
		by performing a
		<i>trustworthy confidential fiduciary</i> service. Essentially, it
		keeps private books for customer “checking” accounts and will transfer
		credits between accounts upon receipt of a properly signed “check”.
		Aren’t these some of the same private services for which Swiss banks
		were once famous – back when privacy was something they could really
		offer? Isn’t Bitcoin more like a bygone Swiss Bank than a commodity or
		a colorful piece of paper?
	</p>

	<p align="center">
		<i> Would a DAC metaphor fit Bitcoin even better than its current
			coin metaphor? </i>
	</p>

	<p>
		<i><b>Distributed Autonomous Corporations (DAC)</b> run without
			any human involvement under the control of an incorruptible set of
			business rules. (That’s why they must be distributed and autonomous.)
			These rules are implemented as publicly auditable open source
			software distributed across the computers of their stakeholders. You
			become a stakeholder by buying “stock” in the company or being paid
			in that stock to provide services for the company. This stock may
			entitle you to a share of its “profits”, participation in its growth,
			and/or a say in how it is run. </i>
	</p>

	<p>
		Bitcoins can be viewed as a small “share” of the total market cap of
		the Bitcoin “corporation”. The “mining” services that validate
		transactions and secure the network are paid for in new bitcoins that
		slowly dilute the “stock” as the corporation’s market cap ebbs and
		flows. You can generally trade your shares for other currencies,
		goods, and services. Operating rules for the corporation cannot be
		changed unless a majority of stakeholders vote for them by switching
		to another version of the software. Interestingly, it is not the
		holders of existing shares that get to make this decision, but only
		those “employees” who are contributing their computer resources
		(mining bots) to run the company.
	</p>

	<p>
		Nothing says a corporation can’t be structured to distribute voting
		rights this way, and that’s exactly what Bitcoin has done.
		Shareholders get equity growth. Employees get voting rights. All
		“revenue” is paid to the employees as compensation for their work.
		There are no profits.
	</p>

	<p align="center">
		<b> Bitcoin is a shareholder-owned, employee run, not-for-profit
			crypto-corporation! </b>
	</p>

	<p>
		“Real” corporations, of course, are legal constructs created by
		governments so that groups of people can act as a single artificial
		person. But groups of people are in general no more trustworthy than
		individuals. Even if you completely eliminate all humans in the
		corporation via automation, the result will no more honorable than the
		rascally humans that control and maintain it. Can we construct a DAC
		that you would trust more than a Swiss Bank? I think Bitcoin may have
		found a way – with distributed autonomous software and its own Three
		Laws of Robotics. If you’re a true Sci-Fi buff, you already know the
		original set by heart:
	</p>

	<p>
	<h4>
		Asimov’s Three Laws of Robotics
	</h4>
	</p>

	<p>
	<ol>
		<li>
			A robot may not injure a human being or, through inaction, allow a
			human being to come to harm.
		</li>
		<li>
			A robot must obey the orders given to it by human beings,except where
			such orders would conflict with the First Law.
		</li>
		<li>
			A robot must protect its own existence as long as such protection
			does not conflict with the First or Second Law.
	</ol>
	</p>

	<p>
		In Isaac Asimov’s
		<a
			href="http://www.amazon.com/Robot-Signet-S1282-Edition-Thus/dp/B003HJ6RJY/ref=pd_sxp_f_i"><i>I,
				Robot</i> </a> universe, every robot was required to have these laws
		hard-wired into its positronic brain as a safety feature. Does Bitcoin
		not have analogous “laws” indelibly etched into its very core? Are not
		these Core Laws what make Bitcoin virtually incorruptible?
	</p>

	<p align="center">
		<b> The critical innovation with distributed blockchain systems
			like Bitcoin is their ability to credibly implement their own set of
			incorruptible Core Laws. </b>
	</p>

	<p>
		Any set of business rules could be implemented by a DAC, even those
		most would consider evil. The key idea is that the Core Laws ensure
		that a chosen set of business laws are burned-in, immutable, and
		incorruptible. If you like a DAC’s incorruptible business rules, you
		can rely on them. If you don’t like them, don’t do business with them.
		But you can never be betrayed.
	</p>

	<p align="center">
		<b> DACs can still be designed to have a robotically inviolable
			intention to rob you blind, but to enter the open source arena they
			must be honest about their plans to do so. </b>
	</p>

	<p>
		What Core Laws should DACs implement and how would they do it? Here’s
		my stake in the ground, with apologies to Dr. Asimov:
	</p>

	<p align="center">
		<b> Three Laws of Robotics for Distributed Autonomous Corporations
		</b>
	</p>

	<p>
	<ol>
		<li>
			A DAC must always obey its own published business rules.
		</li>
		<li>
			A DAC must never change its rules without consent of its
			stakeholders, except where such change would conflict with the First
			Law.
		</li>
		<li>
			A DAC must protect its own existence, as long as such protection does
			not conflict with the first two laws.
		</li>
	</ol>
	I, DACIn actual practice, these Three Laws are embedded into open
	source software for all stakeholders to inspect. They govern the
	protection of the rights of stakeholders and how other rules can be
	changed, but they themselves can never be changed.
	<img style="float: right"
		src="https://static.squarespace.com/static/51fb043ee4b0608e46483caf/t/5230bcb9e4b08c3c01e6a516/1378925754363/I,%20DAC%20Graphic.png?format=500w"
		width="360" height="342" />
	</p>

	<p>
		<b>The First Law, integrity, </b>is enforced by cross checking every
		DAC node’s actions with many other DAC nodes (i.e. bots). Rogue
		actions by individual DAC bots are simply blocked by the collective
		and their perpetrators shunned. Rule disobedience is futile. Coercion
		by nefarious agencies is futile.
	</p>

	<p>
		<b>The Second Law, incorruptibility,</b> ensures updates to any DAC
		source code are not incorporated without consent of a majority of
		stakeholders. Unless more than half of the total collective work force
		agrees to adopt it, corrupting even a large minority of DACBOTs will
		have no effect.
	</p>

	<p>
		<b>The Third Law, self-preservation,</b> covers features engineered to
		harden the software against very real threats to a DAC’s existence.
		The first two laws already mitigate the introduction of bad bots into
		the collective. Open source software has thus far prevented corrupt
		agencies from introducing corrupting “features”. But a more serious
		risk is any persistent tether to a DAC’s human creators who must
		necessarily remain exposed to all the corrupting influences that flesh
		is heir to. Such tethers are easy to exploit by shutting down web
		addresses, passing laws, proliferating regulations, issuing
		injunctions …or much darker methods. Attacks against a DAC’s creators
		remain its Achilles’ heel. To survive as trusted entities, DACs must
		become internationally autonomous.
	</p>

	<p align="center">
		<i><b> Implementing the Three Laws of Robotics for DACs will
				be a never-ending arms race.</b> </i>
	</p>

	<p>
		Asimov had the enviable privilege of decreeing his Three Laws as a
		fait accompli. In the cold, cruel real world, we must tend to the
		engineering details. Integrity, incorruptibility, and
		self-preservation technology must evolve with the escalating threats
		against them. To stand still is to be overrun.
	</p>

	<p>
		One obviously needed self-preservation strategy would be to include
		with each DAC’s distribution package a massively distributed ability
		to propagate that distribution package. DACBOTs need to be able to
		clone more identical DACBOTs without the interdictable support of
		their creators. Another desperately needed innovation is a better way
		to introduce inevitable software updates that affect all DACBOTs.
		Software updates generally rely on trusting the developer. But now, it
		is way too likely that any trusted DAC provider will be shut down,
		prevented from offering its software, or seduced/coerced into
		installing back doors for Nefarious Secret Agencies in their next
		maintenance release. Developers are, after all, only human.
	</p>



