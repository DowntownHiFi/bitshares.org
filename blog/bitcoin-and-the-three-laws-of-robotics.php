	<p>
		To date, Bitcoin has been well protected from such Trojan horses by
		its open source policy and a worldwide network of anonymous,
		independent Steely-Eyed Geeks (SEG) who would love to be the first to
		blow the whistle. Unfortunately, Steely-Eyed Geeks are a scarce
		resource that must be attracted like antibodies to a DACs defense. As
		DACs proliferate, novelty and fame will be less effective in
		attracting such resources. So DACs will need to find ways to
		autonomously compensate their human defenders. Direct hiring by the
		DAC’s creators would expose SEGs to the same seduction and coercion
		risks. To be effective, SEGs must remain motivated, anonymous and
		independent. Distributed Autonomous Corporations need a SEG benefits
		package!
	</p>

	<p>
		Ultimately, to achieve complete incorruptibility, developers must be
		willing to let go of their own control. If there remains any
		centralized human control anywhere, it will eventually be exploited to
		the detriment of the DAC’s stakeholders. DACs need to be free to be
		trusted.
	</p>

	<p align="center">
		<i><b> “Centralization tends to corrupt, and absolute
				centralization corrupts absolutely.”</b> </i>
	</p>

	<p>
		Bitcoin has already demonstrated its ability to credibly implement a
		social contract based on a rudimentary set of inviolable Core Laws. We
		are betting that, having recognized they are in an arms race,
		developers will continue to out-distance the necessarily reactive
		forces that will try to corrupt them. Put another way:
		<i>“incorruptibility is a necessity and necessity is the mother of
			invention.”</i>
	</p>

	<p>
		DACS with superhuman robot honor are inevitable. It’s their unique
		competitive edge.
	</p>

	<p>
	<h2>
		The Future of DACS
	</h2>
	</p>

	<p>
		The DAC metaphor seems to be helpful in appreciating the revolutionary
		nature of blockchain technology. Its not just the sine quo non of
		digital currency, it’s a way to implement incorruptible business
		relationships of almost any kind. Adopting this new metaphor is like
		the difference between using a flat earth or a sphere as your model
		for planning global shipping routes. All sorts of new insights open up
		when we tap our prior experience about how market-driven corporations
		work in the physical world. Lets go there in two steps: First, try to
		imagine the value of a corporate entity you can design to have
		robotically inviolable fiduciary regard for the interests of its
		customers and stakeholders. Second, lets consider how to make it
		operate like a traditional corporation optimized to compete with its
		shifty human peers.
	</p>

	<p>
		<b>Step 1: Recognize the nature of the beast.</b> Relentlessly
		honorable autonomous corporations would have many advantages in the
		free market. Like Bitcoin, DACs armed with a set of inviolable Core
		Laws are far safer to deal with than corruptible human organizations.
		Moreover, such entities have emergent characteristics with far more
		interesting applications than just keeping the books for a crypto
		currency. Blockchain ethics allow a suitably motivated engineer to
		“incorporate” some or all of the following SEG-verifiable
		characteristics.
	</p>

	<p>
	<ol>
		<li>
			They are corporations – They are, and of a right ought to be, free
			and independent persons.
		</li>
		<li>
			They are autonomous – once up to speed; they no longer need (or heed)
			their creators.
		</li>
		<li>
			They are distributed – there are no central points of control or
			failure that can be attacked.
		</li>
		<li>
			They are transparent – their books and business rules are auditable
			by all.
		</li>
		<li>
			They are confidential – customer information is securely (and
			incorruptibly) protected.
		</li>
		<li>
			They are trustworthy – because no interaction with them depends on
			trust.
		</li>
		<li>
			They are fiduciaries – acting solely in their customers’ and
			shareholders’ interests.
		</li>
		<li>
			They are self-regulating – they obey their own rules like, well,
			robots.
		</li>
		<li>
			They are incorruptible – no one can exercise seductive or coercive
			influence over them.
		</li>
		<li>
			They are sovereign – over their digital resources. They don’t need
			governments to exist.
		</li>
	</ol>
	</p>

	<p>
		Collectively these imply that:
	</p>

	<p align="center">
		<i><b> DACs don’t need regulation, you don’t want to regulate
				them, and happily you can’t. 
		</i></b>
	</p>

	<p>
		A DAC can be introduced into the wild from anywhere in the world and
		will spread on its own to wherever its services are appreciated. It
		will survive if it is well adapted to its market environment and
		perish if not. Its creator can improve its survival odds by promoting
		it and defending it up to a point. But the umbilical cord should be
		quickly severed since its creator remains exposed to the risks of the
		real world.
	</p>

	<p>
		This is not to say that DACs are above the
		<i>Law of the Market.</i> Such natural law is the most legitimate
		source of regulation. If a DAC doesn’t provide trustworthy value in
		the marketplace, it will be shunned and die of neglect. If its
		published social contract and the services it offers don’t make it a
		desirable business partner, it will perish.
	</p>

	<p>
		In the end, a DAC’s survival depends on the willingness of others to
		engage in voluntary exchange with it. DACs, like people, will develop
		reputations. The Core Laws make sure that once a reputation is earned,
		it can be counted on to persist. History is littered with human
		organizations that started out with high ideals like “We the people”
		or “Don’t be evil” only to succumb to ethics rot as flesh and blood
		executives are seduced and/or coerced. Inviolable DAC ethics can start
		out bad, but they can’t rot.
	</p>

	<p>
		Like the industrial and Internet revolutions before it, the DAC
		revolution will be a boon to those jurisdictions that restrict it the
		least. Would-be regulators can’t stop the world’s truly free markets
		from adopting it. They can only make it harder for their own hobbled
		markets to benefit. Perhaps that’s a price they are willing to pay for
		power.
	</p>

	<p align="center">
		<i><b> Regulation by force, like the use of antibiotics, will
				only lead to new, more resistant strains. 
		</i></b>
	</p>

	<p>
		Namecoin, Mastercoin, BitShares and other “crypto-entities” already
		share many of these characteristics though some may not fully realize
		it. New insights and design patterns can be gained by recognizing (and
		perhaps standardizing) the DAC metaphor. Such standards (and a good
		benefits package) will focus and conserve critical and limited Steely
		Eyed Geek resources.
	</p>

	<p>
		<b>Step 2: Exploit the utility of the beast. </b> Technologies for
		making impeccably honest and tamper-proof software are only valuable
		if the software does something useful. If we use a currency metaphor,
		we can invent better currencies. If we use a corporation metaphor, we
		can invent almost anything. A DAC is a company and can work in many
		ways like those of brick and mortar and flesh and blood.
	</p>

	<p>
	<ol>
		<li>
			It can raise capital by selling shares of its stock … and pay
			dividends.
		</li>
		<li>
			It can raise capital by borrowing against its stock … and pay
			interest.
		</li>
		<li>
			It can raise capital by providing services of value to its customers.
		</li>
		<li>
			It can use stock to pay persons (human or artificial) for resources
			and services.
		</li>
		<li>
			It can use Internet APIs to automate access to its products and
			services.
		</li>
		<li>
			It can adapt its business processes in response to market feedback.
		</li>
		<li>
			It can use stock transfers to (fairly) reward or penalize the
			behavior of others.
		</li>
	</ol>
	</p>

	<p>
		As a DAC performs fiduciary services of value to others it generates
		real wealth that can be stored and transferred via its own stock. But,
		as Clint Eastwood might say, “a DAC’s gotta know its limitations.” A
		sovereign DAC has no way to accept, hold, or remit physical
		commodities or fiat currencies and still remain independent of
		external forces. Further, an open source DAC can’t keep a secret. It
		can securely hold encrypted data for others, but it has no place to
		keep a private key of its own. Thus, a DAC can’t have its own crypto
		wallet full of blockchain assets.
	</p>

	<p>
		So it works best when creating or transferring value entirely within
		the DAC domain. This is not to say that humans can’t trade real assets
		<i>among themselves </i> in exchange for rights to a DAC’s shares, or
		that a DAC can’t participate in the allocation and tracking of those
		rights. But its sovereignty extends only over the contents of its own
		blockchain.
	</p>

	<p>
		Here are some notional DACs that might well appear in the coming year
		…or decade. Naturally, their corporate names in the DAC Domain would
		end in DAC, not INC, LTD, or LLC. (Folks will want to know if they are
		dealing with a Corporation of Superhuman Integrity.) The common
		denominator is they all exploit an emerging competitive edge:
	</p>

	<p>
		<i>DACs trade on their incorruptible ability to apply transparent
			rules to secure data.</i>
	</p>

