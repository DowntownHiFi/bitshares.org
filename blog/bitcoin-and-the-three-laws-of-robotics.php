		</li>
		<li>

			Virtual Ventures, DAC – A crowd-sourced venture capital firm.
		</li>
		<li>


			Autonomous Arbitrators, DAC – Incorruptible arbitration services.
		</li>
		<li>

			DAC Installers R Us, DAC – Trustworthy auto-installation of
			consensus-based DAC updates.
		</li>
		<li>


			SkyNet, DAC – A swarm of DACBOT satellites implementing a unbuggable
			new Internet.
		</li>
		<li>


			One World Government, DAC – A government that can’t ignore its own
			constitution.

		</li>
	</ol>
	<p>
		I’ll quit before I get carried away. The point is, changing metaphors
		opens up unlimited new possibilities. You’re not limited to just
		another altcoin. What earth-shaking, paradigm-busting DAC could you
		release into the wild?
	</p>

	<p>
		<b>Bottom Line.</b> Metaphorically, the concept of a Distributed
		Autonomous Corporation is a helpful way to understand the much greater
		potential of the technology pioneered by Bitcoin. By moving from the
		metaphor of a digital currency to one of an incorruptible unmanned
		business, revolutionary new entities can be envisioned. Unlike their
		flesh and blood peers, DACs will be sovereign corporations governed
		with inhuman integrity.
	</p>

	<p>
		<b><i>And that should make all the difference.</i> </b>
	</p>

