<div class="schedule">
          <h2> Schedule </h2>
          <p> Due to the unpredictability of software development we do not commit
              to specific release dates.  Instead we will outline the roadmap and
              milestones between where we are today and the release of BTS X.</p>

          <h3> Roadmap </h3>
          <ol>
               <li><h4>Release <a href="/bitshares/toolkit">bitshares toolkit</a></h4>
                   <p>
                   The bitshares toolkit is a framework for building DACs and is the foundation of
                   every bitshares DAC.  This toolkit must be complete so that BTS X can receive the
                   benefits of work being done on all other DACs.
                   </p>
               </li>
               <li><h4>Release bitshares XT</h4>
                    <p> BitShares XT is the test network that will make the
                    BTS X snapshot taken on Feb 28th liquid and test critical
                    components of the basic blockchain infrastructure.  This will
                    be the first blockchain based upon the <a href="/bitshares/toolkit">bitshares toolkit</a>.
                    </p>
               </li>
               <li><h4>Release bitshares ME</h4>
                    <p> bitshares ME allows users to issue personal shares and features
                    a built in trading engine.  Releasing BTS ME first will enable us to
                    test core features of the matching algorithm prior to adding in the
                    more complex and risky features of BTS X <a href="/faq/index.php#shorting">shorting</a> and margin calls.
                    </p>
               </li>
               <li><h4>Release bitshares X</h4>
                    <p> After all of the foundational features have been proven the BTS X will be launched with the 
                    following <a href="/faq/index.php#bitasset">BitAssets</a>: BitUSD, BitGold, and BitBTC.
                    </p>
               </li>
          </ol>
</div>
