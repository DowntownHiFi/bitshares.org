<div class="schedule">
          <h2> Schedule </h2>
          <p> Due to the unpredictability of software development we do not commit
              to specific release dates.  Instead we will outline the roadmap and
              milestones between where we are today and the release of BTS DNS.</p>

          <h3> Roadmap </h3>
          <ol>
               <li><h4>Release <a href="/bitshares/toolkit">bitshares toolkit</a></h4>
                   <p>
                   The bitshares toolkit is a framework for building DACs and is the foundation of
                   every bitshares DAC.  This toolkit must be complete so that BTS DNS and .p2p can receive the
                   benefits of work being done on all other DACs.
                   </p>
               </li>
               <li><h4>Release bitshares DNS after XT is stable</h4>
                    <p>Release the proto-DAC for DNS after sufficient testing of bitshares_toolkit core functionality</p>
               </li>
               <li><h4>Release .p2p</h4>
                    <p>Soon after releasing DNS, release .p2p initialized with DNS genesis block as 
                       the snapshot (in other words, using the AGS/PTS snapshot).  </p>
               </li>
          </ol>
</div>
